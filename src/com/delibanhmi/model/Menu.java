package com.delibanhmi.model;

public class Menu {
private int idmenu;
private String nomderepas;
private String inscription;
private int prix;
private String typederepas;
public int getIdmenu() {
	return idmenu;
}
public void setIdmenu(int idmenu) {
	this.idmenu = idmenu;
}
public String getNomderepas() {
	return nomderepas;
}
public void setNomderepas(String nomderepas) {
	this.nomderepas = nomderepas;
}
public String getInscription() {
	return inscription;
}
public void setInscription(String inscription) {
	this.inscription = inscription;
}
public int getPrix() {
	return prix;
}
public void setPrix(int prix) {
	this.prix = prix;
}
public String getTypederepas() {
	return typederepas;
}
public void setTypederepas(String typederepas) {
	this.typederepas = typederepas;
}

}
